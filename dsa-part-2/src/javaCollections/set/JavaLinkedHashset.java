package javaCollections.set;

import java.util.LinkedHashSet;

public class JavaLinkedHashset {

	public static void main(String[] args) {
		
		
		LinkedHashSet<String> lhs= new LinkedHashSet<>();
		
		lhs.add("ghi");
		lhs.add("abc");
		lhs.add("def");
		lhs.add("jkl");
		lhs.add("mno");
		
		System.out.println(lhs);
		
		
		

	}

}
